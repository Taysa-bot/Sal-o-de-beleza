    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        String nome = jTextFieldNome.getText();
        int cargaHoraria = Integer.parseInt(jTextFieldCargaHoraria.getText());
        double salario = Double.parseDouble(jTextFieldSalario.getText());
        Profissional profissao = new Profissional(nome, salario, cargaHoraria);
        profissionais.add(profissao);
        JOptionPane.showMessageDialog(this, "Profissional cadastrada");

        DefaultTableModel dtmProfissional = (DefaultTableModel) jTableCadastrarProfissional.getModel();
        Object[] dados = {jTextFieldNome.getText(), jTextFieldSalario.getText(), jTextFieldCargaHoraria.getText()};
        dtmProfissional.addRow(dados);

    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        if(jTableCadastrarProfissional.getSelectedRow()!= -1){
        DefaultTableModel dtmProfissional = (DefaultTableModel) jTableCadastrarProfissional.getModel();
        dtmProfissional.removeRow(jTableCadastrarProfissional.getSelectedRow());
        }else{
            JOptionPane.showMessageDialog(this, "Selecione uma linha para excluir");
        }
       
        
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jTableCadastrarProfissionalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableCadastrarProfissionalMouseClicked
        if(jTableCadastrarProfissional.getSelectedRow()!= -1){
            jTextFieldNome.setText(jTableCadastrarProfissional.getValueAt(jTableCadastrarProfissional.getSelectedRow(), 0).toString());
            jTextFieldSalario.setText(jTableCadastrarProfissional.getValueAt(jTableCadastrarProfissional.getSelectedRow(), 1).toString());
            jTextFieldCargaHoraria.setText(jTableCadastrarProfissional.getValueAt(jTableCadastrarProfissional.getSelectedRow(), 2).toString());
        }else{
            JOptionPane.showMessageDialog(this, "selecione uma linha para atualizar");
        }
    }//GEN-LAST:event_jTableCadastrarProfissionalMouseClicked

    private void jTableCadastrarProfissionalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableCadastrarProfissionalKeyReleased
        if(jTableCadastrarProfissional.getSelectedRow()!= -1){
            jTextFieldNome.setText(jTableCadastrarProfissional.getValueAt(jTableCadastrarProfissional.getSelectedRow(), 0).toString());
            jTextFieldSalario.setText(jTableCadastrarProfissional.getValueAt(jTableCadastrarProfissional.getSelectedRow(), 1).toString());
            jTextFieldCargaHoraria.setText(jTableCadastrarProfissional.getValueAt(jTableCadastrarProfissional.getSelectedRow(), 2).toString());
        }else{
            JOptionPane.showMessageDialog(this, "selecione uma linha para atualizar");
        }
    }//GEN-LAST:event_jTableCadastrarProfissionalKeyReleased

    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        if(jTableCadastrarProfissional.getSelectedRow()!= -1){
            jTableCadastrarProfissional.setValueAt(jTextFieldNome.getText(), jTableCadastrarProfissional.getSelectedRow(), 0);
            jTableCadastrarProfissional.setValueAt(jTextFieldSalario.getText(), jTableCadastrarProfissional.getSelectedRow(), 1);
            jTableCadastrarProfissional.setValueAt(jTextFieldCargaHoraria.getText(), jTableCadastrarProfissional.getSelectedRow(), 2);
        }else{
            JOptionPane.showMessageDialog(this, "selecione uma linha para atualizar");
        }
    }//GEN-LAST:event_jButtonAtualizarActionPerformed


